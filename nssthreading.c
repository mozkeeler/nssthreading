unsigned int server_der_len = 763;

int
doNewTempCertificate()
{
  SECItem certDER = { siBuffer, server_der, server_der_len };
  CERTCertificate* cert = CERT_NewTempCertificate(CERT_GetDefaultCertDB(),
                                                  &certDER, NULL, PR_FALSE,
                                                  PR_TRUE);
  if (!cert) {
    printPRError("CERT_NewTempCertificate");
    return 1;
  }
  CERT_DestroyCertificate(cert);
  return 0;
}

int stop = 0;

void
newTempCertificateThreadMain(void* arg)
{
  while (1) {
    doNewTempCertificate();
    PR_Sleep(rand() % 125);
  }
}

void
UnloadRoots()
{
  SECMODModule* module = SECMOD_FindModule("Builtin Roots Module");
  if (!module) {
    printPRError("SECMOD_FindModule");
    return;
  }
  SECMOD_UnloadUserModule(module);
  SECMOD_DestroyModule(module);
}

void
LoadRoots()
{
  SECMODModule* module = SECMOD_LoadUserModule(
    "name=\"Builtin Roots Module\", library=\"dist/Linux3.13_x86_64_clang-3.9_glibc_PTH_ASAN_64_DBG.OBJ/lib/libnssckbi.so\"",
    NULL, 0);
  if (!module) {
    printPRError("SECMOD_LoadUserModule");
    return;
  }
  SECMOD_DestroyModule(module);
}

void
rootsThreadMain(void* arg)
{
  while (1) {
    LoadRoots();
    PR_Sleep(rand() % 113);
    UnloadRoots();
    PR_Sleep(rand() % 97);
  }
}

int
main(int argc, char* argv[])
{
  if (NSS_Init(".") != SECSuccess) {
    printPRError("NSS_Init");
    return 1;
  }
  PRThread* newTempCertificateThread =
    PR_CreateThread(PR_USER_THREAD, newTempCertificateThreadMain, NULL,
                    PR_PRIORITY_NORMAL, PR_GLOBAL_THREAD, PR_UNJOINABLE_THREAD,
                    0);
  if (!newTempCertificateThread) {
    printPRError("PR_CreateThread");
    return 1;
  }
  PRThread* rootsThread = PR_CreateThread(PR_USER_THREAD, rootsThreadMain,
                                          NULL, PR_PRIORITY_NORMAL,
                                          PR_GLOBAL_THREAD,
                                          PR_UNJOINABLE_THREAD, 0);
  if (!rootsThread) {
    printPRError("PR_CreateThread");
    return 1;
  }
  while (!stop) {
    PR_Sleep(2000);
  }
  if (NSS_Shutdown() != SECSuccess) {
    printPRError("NSS_Shutdown");
    return 1;
  }
  return 0;
}
